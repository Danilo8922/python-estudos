n = float(input('digite um numero '))
n2 = 2
soma = n + n2 
subitração = n - n2
mutiplicação = n * n2
divisão = n / n2
potência = n ** n2
divisão_inteira = n // n2
resto_da_divisão = n % n2
print('A soma do seu numero {} entre {} é {}' .format(n, n2, soma),)
print('A subitração do seu numero {} entre {} é {} ' .format(n, n2,subitração))
print('A multiplicação do seu numero {} entre {} é {}' .format(n, n2, mutiplicação))
print('A divisão do seu numero {} entre {} é {}' .format(n, n2, divisão)) 
print('O seu numero {} elevado a {} é {}' .format(n, n2, potência)) 
print('A divisão inteira do seu numero {} entre {} é {}' .format(n, n2, divisão_inteira)) 
print('e o resto da divisão do seu numero {} entre {} é {}' .format(n, n2, resto_da_divisão)) 
